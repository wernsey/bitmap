static void tt_putc(Bitmap *b, int x, int y, unsigned short c) {
	int frow, fcol, byte;
    unsigned int col;
    int i, j;
    if(c < 32 || c > 127) c = 127;
    c -= 0x20;
    fcol = c >> 3;
    int shf = (fcol & 0x1);
    fcol >>= 1;    
    frow = c & 0x7;    
    byte = frow * 5 * 6 + fcol;
    col = bm_get_color(b);
    for(j = 0; j < 5 && y + j < b->clip.y1; j++) {
        if(y + j >= b->clip.y0) {
            unsigned char bits = tom_thumb_bits[byte];
            if(shf) bits >>= 4;
            for(i = 0; i < 4 && x + i < b->clip.x1; i++) {
                if(x + i >= b->clip.x0 && !(bits & (1 << i))) {
                    bm_set(b, x + i, y + j, col);
                }
            }
        }
        byte += 6;
    }
}

static int tt_puts(Bitmap *b, int x, int y, const char *s) {
	int x0 = x;
	while(*s) {
		if(*s == '\n') {
			y += 6;
			x = x0;
		} else if(*s == '\b') {
			if(x > x0) x -= 4;
		} else if(*s == '\r') {
			x = x0;
		} else if(*s == '\t') {
			x += 4 * 4;
		} else {
			tt_putc(b, x, y, *s);
            x += 4;
        }
		s++;
	}
	return 1;
}

static int tt_width(struct bitmap_font *font) {
    return 4;
}
static int tt_height(struct bitmap_font *font) {
    return 6;
}

BmFont tomthumb = {
    "TOMTHUMB",
    tt_puts,
    tt_width,
    tt_height,
    NULL
};

